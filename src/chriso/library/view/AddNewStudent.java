    private void jButton_registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_registerActionPerformed
         //fetch all the form data of the new student record 
       String firstName="", lastName="",userName="", email="", password="", rePassword="",
               department="",gender="", imageUrl="",dobString="";
       int batch=0;
       Long contact=null;
       java.sql.Date sqldob=null;
   
       
       try{
       try{ firstName = jTextField_firstName.getText();
        lastName = jTextField_lastName.getText();
        userName = jTextField_userName.getText();
        email = jTextField_email.getText();
        password =new String(jPasswordField_password.getPassword());
        rePassword = new String(jPasswordField_rePassword.getPassword());
        department = jTextField_department.getText();
        batch =Integer.parseInt(jTextField_batch.getText());
        contact = Long.parseLong(jTextField_contact.getText());
        imageUrl = jTextField3.getText();
        gender= getGender(); //function is created below
        //this will return the String associated with the selected radio of buttonGroup_gender;
       }
       catch(Exception ex){
           JOptionPane.showMessageDialog(null,"Required field are either empty or have wrong input value");
      
       }
        //to create the Date object from the spinner
        dobString = jSpinner_year.getValue()+"-"+jSpinner_month.getValue()+"-"+jSpinner_day.getValue(); 
        DateFormat dFormat = new SimpleDateFormat("yy-MM-dd",Locale.ENGLISH);
        Date dob = dFormat.parse(dobString); //can generate ParserException
        sqldob = new java.sql.Date(dob.getTime());
        //now creating the object of the Student to store all information
       

    //check if any of the field is empty if not then only make the value of all input
        //correct is true;
                    if(firstName.equals("") && lastName.equals("") && userName.equals("") && email.equals("")
                            && password.equals("") && gender.equals("") && dobString.equals("") && rePassword.equals("")){
                            ALL_INPUT_CORRECT = false;
                            JOptionPane.showMessageDialog(null,"Necessary Field are Still Empty");
                            return;
                      }
                    else{
                        ALL_INPUT_CORRECT = true;
                    }
       }//end of the try block
    catch(ParseException ex){
        System.out.println("from the addnewStudent class date handling : "+ex);   
       }//end of the catch block    
     if(ALL_INPUT_CORRECT && jCheckBox_notRobot.isSelected()&& (sqldob.getTime()<System.currentTimeMillis()) && email.contains("@")){ 
        //when all the input type is correct and not robot is also checked then
        //its ready to insert the record;
        
         
       Student std = new Student();
        std.setFirstName(firstName);
        std.setLastName(lastName);
        std.setUserName(userName);
        std.setBatch(batch);
        std.setDateOfBirth(sqldob);
        std.setPassword(password);
        std.setContact(contact);
        std.setGender(gender);
        std.setEmail(email);
        std.setDepartment(department);
        std.setUrl(imageUrl);
        //now sending the data to the StudentDAO to doing insertion task
        StudentDAO stdao = new StudentDAO();
        boolean success;
        success = stdao.insertStudentRecord(std);
                    if(success){ 
                        JOptionPane.showMessageDialog(null,"record inserted");
                    } 
                    else{
                        System.out.println("error in the insertion");
                    }
      
           }//end of if block when all the  input typeed is correct and the 
    //i am not a robot checkbox is checked;
    else{
        if(!jCheckBox_notRobot.isSelected()){
            JOptionPane.showMessageDialog(null,"Can't insert your record "+AdminDAO.CURRENT_ADMIN_NAME);
        }
        if(sqldob.getTime()<System.currentTimeMillis()){
                      JOptionPane.showMessageDialog(null,"Invalid Data");
  
        }
        if(!email.contains("@")){
                        JOptionPane.showMessageDialog(null,"Can't insert your record Invalid Email");

        }
              
        
    }
    }//GEN-LAST:event_jButton_registerActionPerformed

