    public void drawComponentForSubjectInput(int subjectNo) {
        removeComponents();
        GridBagLayout gbl = new GridBagLayout();
        GridBagConstraints gbc = new GridBagConstraints();
        jPanel_card2.setLayout(gbl);
        int y = 0;
        gbc.gridheight = gbc.gridwidth = 1;
        for (int i = 1; i <= subjectNo; i++) {
            JLabel l1 = new JLabel("Subject " + i + " : ");
            gbc.gridx = 0;
            gbc.gridy = y;
            gbl.setConstraints(l1, gbc);
            jPanel_card2.add(l1);

            JTextField t1 = new JTextField(20);
            gbc.gridx = 1;
            gbc.gridy = y++;
            gbl.setConstraints(t1, gbc);
            jPanel_card2.add(t1);
            t1.addFocusListener(new FocusAdapter(){
                public void focusLost(FocusEvent e){
                    if(t1.getText().equals("")){
                        t1.requestFocus();
                       }
                    else{
                        //add the subject name in the array list
                        ExamControllerFrame.subjectList.add(t1.getText());
                  }
                }
            });
            

  //             jPanel_card2.validate();
            //   jPanel_card2.repaint();
            System.out.println(i + " th control drawn");
        } //end of the for loop
        COMPONENT_DRAWN = true;
    }

