    private void jButton_nextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_nextActionPerformed
        // TODO add your handling code here:
        if (jButton_next.getText().equals("< Back")) {
            card.show(jPanel_container, "one");
            jButton_next.setText("Next >");
            jButton_finish.setEnabled(false);
            return;
        }
        int subjectNo;
        String sheetName = jTextField_sheetName.getText();
        String val = jTextField_subjectNo.getText();
        if (!val.equals("")) {
            subjectNo = Integer.parseInt(val);
        } else {
            subjectNo = 0;
        }
        String examTitle = jTextField_examTitle.getText();
        String folioType = getFolioType();

        if (subjectNo == 0 || sheetName.equals("") || examTitle.equals("") || folioType.equals("")) {
            JOptionPane.showMessageDialog(null, "All fields are Required");
        } else {//when everything is file 
            
            jButton_next.setText("< Back");
            card.show(jPanel_container, "two");
            jButton_finish.setEnabled(true);
            
        //also update the value in the ExamControllerFrame;
        ExamControllerFrame.sheetName =sheetName;
        ExamControllerFrame.folioType = folioType;
        ExamControllerFrame.examTitle = examTitle;
        }
    }//GEN-LAST:event_jButton_nextActionPerformed
