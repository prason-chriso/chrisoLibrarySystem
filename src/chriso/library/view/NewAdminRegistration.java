    private void jButton_registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_registerActionPerformed
        // TODO add your handling code here:
        if(!checkCorrect()){
        JOptionPane.showMessageDialog(null,"Necessary Field are Still Empty");    
        } 
        
        //firstkt fetch the data of the form to store in the variable
        String firstName = jTextField_firstName.getText();
        String lastName = jTextField_lastName.getText();
        String userName = jTextField_userName.getText();
        String password = new String (jPasswordField_password.getPassword());
        String reTypePass =new String(jPasswordField_reTypePassword.getPassword());
        String ImageUrl = jTextField_image.getText();
        String email = jTextField_email.getText();
        long contact =Long.parseLong(jTextField_contact.getText().toString());
        String imgUrl  = jTextField_image.getText();
        //Now we have to store the retrieved values of the form to the object of Admin clsss
       
       Admin newAdmin = new Admin();
       newAdmin.setFirstName(firstName);
       newAdmin.setLastName(lastName);
       newAdmin.setUserName(userName);
       newAdmin.setPassword(password);
       newAdmin.setAdminProfileImageUrl(ImageUrl);
       newAdmin.setContact(contact);
       newAdmin.setEmail(email);
     
       
       //now ,we have to send the object to the AdminDAO class so that the task
       //of the insertion, deletion, update and other tasks can be poerformed;
       if(!INPUT_OK)
           return;
       AdminDAO adminDAO = new AdminDAO();
       boolean success = adminDAO.insertAdminRecord(newAdmin);
       if(success){
           JOptionPane.showMessageDialog(null,"Your Record is inserted");
           dispose();//closing the JInternalFrame
        }
       else{
           JOptionPane.showMessageDialog(null,"We can't save your Record");
           jLabel2.setText("Change your Image");
           
       }

    }//GEN-LAST:event_jButton_registerActionPerformed

    private void jButton_uploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_uploadActionPerformed
      JFileChooser jfc = new JFileChooser();
      int result = jfc.showOpenDialog(this);
      if(result == JFileChooser.APPROVE_OPTION){
       File file = jfc.getSelectedFile();
       jTextField_image.setText(file.toString());
      }
    }//GEN-LAST:event_jButton_uploadActionPerformed

    public boolean checkCorrect(){
         String firstName = jTextField_firstName.getText();
        String lastName = jTextField_lastName.getText();
        String userName = jTextField_userName.getText();
        String password = new String(jPasswordField_password.getPassword());
        String email = jTextField_email.getText();
               
        if(firstName.equals("") && lastName.equals("") && userName.equals("") && password.equals("") && email.equals("")){
        INPUT_OK = false;
        return false;
        }
        else{
            INPUT_OK = true;
            return true;
        }
    }
