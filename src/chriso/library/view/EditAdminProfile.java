    private void jButton_updateAdminRecordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_updateAdminRecordActionPerformed

        String firstName = jTextField_firstName.getText();
        String lastName = jTextField_lastName.getText();
        String username = jTextField_userName.getText();
        String email = jTextField_email.getText();
        long contact = Long.parseLong(jTextField_contact.getText());
        String imgUrl = jTextField_imageUrl.getText();
       //storing the data in the admin oobbject ; 
        Admin admin = new Admin();
        admin.setFirstName(firstName);
        admin.setLastName(lastName);
        admin.setEmail(email);
        admin.setContact(contact);
        admin.setUserName(username);
        if(!imgUrl.equals(""))//store this value only if the url is not blank 
        admin.setAdminProfileImageUrl(imgUrl);

        AdminDAO admindao = new AdminDAO();
        admindao.updateAdminProfile(admin);
        displayAdminInfo();//diplay the record after the successfully updating 
    }//GEN-LAST:event_jButton_updateAdminRecordActionPerformed

