    public void displayAdminInfo() { //this funtion will do the task of fetching the data
        //and displaying it in their respective text fields position automatically
        Admin adminData = new Admin();
        AdminDAO admindao = new AdminDAO();
        adminData = admindao.getAllAdminData();
        //now display all the information of the admin whenever the
        //panel to load show the admin update information is loaded
        jTextField_firstName.setText(adminData.getFirstName());
        jTextField_lastName.setText(adminData.getLastName());
        jTextField_userName.setText(adminData.getUserName());
        jTextField_email.setText(adminData.getEmail());
        jTextField_contact.setText(valueOf(adminData.getContact()));

        //bow preparing for the diplay of the image in the respective area of the 
        //edit profile 
        if (adminData.getAdminPhoto()== null) {
            System.out.println("no image of admin is available");
            jLabel9.setIcon(new ImageIcon(getClass().getResource("/Drawable/notAvailable.jpg")));
        } else {
            try {
                Blob blob = adminData.getAdminPhoto();

                int blobLength = (int) blob.length();
                byte[] bytes = blob.getBytes(1, blobLength);
                blob.free();
                BufferedImage img = ImageIO.read(new ByteArrayInputStream(bytes));
                ImageIcon icon = new ImageIcon(img);

                Image newimg=icon.getImage().getScaledInstance(278, 340, BufferedImage.SCALE_DEFAULT);

                jLabel9.setIcon(new ImageIcon(newimg));
                System.out.println("Successfully displayed the image in the label book Cover");
            } catch (Exception ex) {
                System.out.println("while displaying thhe image of the admin in admin edit");
            }
        }

    }

