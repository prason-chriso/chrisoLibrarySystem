    private void jButton_saveRecordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_saveRecordActionPerformed
      try{
         //now we are first extracting the value of the new boook record info form
        long isbn = Long.parseLong(jTextField_isbn.getText());
        int codeFrom = Integer.parseInt(jTextField_codeFrom.getText());
        int codeTo = Integer.parseInt(jTextField_codeTo.getText());
        String title = jTextField_title.getText();
        String author = jTextField_author.getText();
        String status = jComboBox_bookStatus.getSelectedItem().toString();
        int stock = codeTo-codeFrom+1;
        String coverUrl = jTextField_bookCoverURL.getText();
        int bookShelf = Integer.parseInt(jTextField_bookShelf.getText());
        int col = Integer.parseInt(jTextField_column.getText());
        int row = Integer.parseInt(jTextField_row.getText());
            
   if(isbn>0 && codeFrom>=0 && codeTo>=codeFrom && (!title.equals("")) && (!author.equals("")) 
           && (!status.equals("")) && (!coverUrl.equals("")) && bookShelf>=0 && col>=0 && row>=0){
        //now we will create the object of the book and store all the above information
        Book newBook = new Book();
        newBook.setIsbn(isbn);
        newBook.setLocalCodeFrom(codeFrom);
        newBook.setLocalCodeTo(codeTo);
        newBook.setBookTitle(title);
        newBook.setBookAuthor(author);
        newBook.setBookShelf(bookShelf);
        newBook.setCol(col);
        newBook.setRow(row);
        newBook.setStockAvailable(stock);
        newBook.setBookURL(jTextField_bookCoverURL.getText());
        //now preparring the data for the insertion in the child Table LocalBookEntry
        LocalBookEntry lbe = new LocalBookEntry();
        lbe.setIsbn(isbn);
        lbe.setFrom(codeFrom);
        lbe.setTo(codeTo);
        lbe.setBookStatus(status);

        //now submit the above object data to the book DAO class for insertion
       
        BookDAO bd = new BookDAO();
        boolean success = bd.insertBookRecord(newBook, lbe);
        if (success) {
            JOptionPane.showMessageDialog(null, "Book Record Inserted");
        } else {
            
            JOptionPane.showMessageDialog(null,"Insertion Failed"+ErrorOutputHolder.errorText);
        }
    }//end of if all field are required checking
   else{
                   JOptionPane.showMessageDialog(null,"All field Are needed here....."+ErrorOutputHolder.errorText);

   }
   
      }//end of try
      catch(Exception ex){
                        JOptionPane.showMessageDialog(null,"All field Are needed here...with valid input"+ErrorOutputHolder.errorText);

      }

    }//GEN-LAST:event_jButton_saveRecordActionPerformed

